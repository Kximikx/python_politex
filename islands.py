from collections import deque  # імпортуємо deque 


def islands(g):  # функція, яка повертає тільки кількість островів
    return label_islands(g)[0]  # беремо перший елемент 


def label_islands(g, diag=False):  # рахує острови і будує матрицю міток
    if not g or not g[0]:  # перевірка: якщо матриця порожня
        return 0, []  # островів 0 міток нема

    n = len(g)  # кількість рядків
    m = len(g[0])  # кількість стовпців
    lab = [[0] * m for _ in range(n)]  # матриця міток 0 = вода/непомічено
    cur_id = 0  # поточний номер острова

    if diag:  # якщо треба рахувати ще діагоналі
        dirs = [(-1, 0), (1, 0), (0, -1), (0, 1),  # 4 напрямки
                (-1, -1), (-1, 1), (1, -1), (1, 1)]  # + 4 діагоналі
    else:  # стандартно лише 4 напрямки
        dirs = [(-1, 0), (1, 0), (0, -1), (0, 1)]  # вгору/вниз/ліво/право

    for i in range(n):  # проходимо всі рядки
        for j in range(m):  # проходимо всі стовпці
            if g[i][j] == 1 and lab[i][j] == 0:  # якщо це суша і ще не мічена
                cur_id += 1  # знайшли новий острів  новий номер
                q = deque([(i, j)])  # черга BFS зі стартовою клітинкою
                lab[i][j] = cur_id  # помічаємо клітинку номером острова

                while q:  # поки є клітинки для обходу
                    x, y = q.popleft()  # беремо наступну клітинку з черги
                    for dx, dy in dirs:  # перебираємо напрямки руху
                        nx, ny = x + dx, y + dy  # координати сусіда
                        if 0 <= nx < n and 0 <= ny < m:  # перевірка меж
                            if g[nx][ny] == 1 and lab[nx][ny] == 0:  # суша і не мічена
                                lab[nx][ny] = cur_id  # мітимо тим самим островом
                                q.append((nx, ny))  # додаємо сусіда в чергу

    return cur_id, lab  # повертаємо кількість островів і матрицю міток


def island_sizes(g, diag=False):  # повертає розміри островів
    k, lab = label_islands(g, diag)  # отримуємо кількість і мітки
    if k == 0:  # якщо островів нема
        return []  # повертаємо порожній список

    sizes = [0] * (k + 1)  # sizes[id] = площа острова з номером id
    for row in lab:  # проходимо по рядках міток
        for x in row:  # проходимо по клітинках
            if x != 0:  # якщо це не вода
                sizes[x] += 1  # збільшуємо площу відповідного острова
    return sizes[1:]  # повертаємо без нульового елемента


def read_grid():  # зчитує матрицю з консолі
    n, m = map(int, input().split())  # читаємо n і m з першого рядка
    g = []  # список для матриці
    for _ in range(n):  # читаємо n рядків
        g.append(list(map(int, input().split())))  # додаємо рядок матриці
    return g  # повертаємо матрицю


def print_grid(g):  # друк матриці в консоль
    for row in g:  # кожен рядок
        print(" ".join(map(str, row)))  # друкуємо числа через пробіл


if __name__ == "__main__":  # якщо запускаємо файл напряму
    g = read_grid()  # читаємо матрицю

    k, lab = label_islands(g, diag=False)  # рахуємо острови і мітимо їх (4 напрями)
    s = island_sizes(g, diag=False)  # рахуємо розміри островів

    print("ISLANDS:", k)  # виводимо кількість островів
    print("LABELS:")  # заголовок для міток
    print_grid(lab)  # друкуємо матрицю міток
    print("SIZES:", " ".join(map(str, s)))  # друкуємо розміри островів