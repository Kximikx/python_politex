class DSU:  # клас для Union-Find (об'єднання племен)
    def __init__(self):  # конструктор
        self.p = {}  # словник "батько" для кожного елемента
        self.r = {}  # словник "ранг" (приблизна висота дерева)

    def add(self, x):  # додати людину в DSU, якщо її ще нема
        if x not in self.p:  # якщо x ще не додавали
            self.p[x] = x  # батько самого себе
            self.r[x] = 0  # ранг спочатку 0

    def find(self, x):  # знайти корінь множини (племені) для x
        if self.p[x] != x:  # якщо x не є коренем
            self.p[x] = self.find(self.p[x])  # стискаємо шлях до кореня
        return self.p[x]  # повертаємо корінь

    def union(self, a, b):  # об'єднати множини a і b
        ra = self.find(a)  # корінь племені для a
        rb = self.find(b)  # корінь племені для b
        if ra == rb:  # якщо вже в одному племені
            return  # нічого не робимо
        if self.r[ra] < self.r[rb]:  # якщо ранг ra менший
            ra, rb = rb, ra  # міняємо місцями, щоб ra був "вищий"
        self.p[rb] = ra  # підвішуємо rb до ra
        if self.r[ra] == self.r[rb]:  # якщо ранги були однакові
            self.r[ra] += 1  # збільшуємо ранг нового кореня


def main():  # головна функція
    n = int(input().strip())  # читаємо кількість пар N
    dsu = DSU()  # створюємо DSU

    for _ in range(n):  # читаємо N рядків
        a, b = map(int, input().split())  # читаємо пару людей
        dsu.add(a)  # додаємо a в DSU
        dsu.add(b)  # додаємо b в DSU
        dsu.union(a, b)  # об'єднуємо їх у одне плем'я

    cb = {}  # словник: корінь -> кількість хлопців у племені
    cg = {}  # словник: корінь -> кількість дівчат у племені
    tb = 0  # загальна кількість хлопців
    tg = 0  # загальна кількість дівчат

    for x in dsu.p.keys():  # проходимо по всіх людях, яких бачили
        r = dsu.find(x)  # знаходимо плем'я (корінь) цієї людини
        if x % 2:  # якщо число непарне (хлопець)
            cb[r] = cb.get(r, 0) + 1  # додаємо хлопця в його плем'я
            tb += 1  # збільшуємо загальну кількість хлопців
        else:  # інакше парне (дівчина)
            cg[r] = cg.get(r, 0) + 1  # додаємо дівчину в її плем'я
            tg += 1  # збільшуємо загальну кількість дівчат

    bad = 0  # кількість пар всередині одного племені (заборонені)
    roots = set(dsu.find(x) for x in dsu.p.keys())  # множина всіх племен (коренів)
    for r in roots:  # для кожного племені
        bad += cb.get(r, 0) * cg.get(r, 0)  # хлопці*дівчата в цьому племені

    print(tb * tg - bad)  # всі пари мінус заборонені (всередині племені)


if __name__ == "__main__":  # якщо запускаємо файл напряму
    main()  # викликаємо main